func readMetadata(t *testing.T, outDir, name string) map[string]any {
	t.Helper()
	return decodeMetadata(t, filepath.Join(outDir, "metadata", name))
}

// readOnlyMetadata reads the sole metadata file under outDir. Use it when the
// filename slug derives from an unpredictable path (e.g. a temp dir).
func readOnlyMetadata(t *testing.T, outDir string) map[string]any {
	t.Helper()
	entries, err := os.ReadDir(filepath.Join(outDir, "metadata"))
	if err != nil {
		t.Fatalf("read metadata dir: %v", err)
	}
	if len(entries) != 1 {
		t.Fatalf("got %d metadata files, want exactly 1", len(entries))
	}
	return decodeMetadata(t, filepath.Join(outDir, "metadata", entries[0].Name()))
}

func decodeMetadata(t *testing.T, path string) map[string]any {
	t.Helper()
	data, err := os.ReadFile(path)
	if err != nil {
		t.Fatalf("read metadata %s: %v", path, err)
	}
	var meta map[string]any
	if err := json.Unmarshal(data, &meta); err != nil {
		t.Fatalf("metadata is not valid JSON: %v", err)
	}
	return meta
}
